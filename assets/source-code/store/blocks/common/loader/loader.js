export default {

  namespace: 'loader',


  state: {
    visible: false,
  },


  mutations: {

    LOADER_VISIBLE_CHANGE({ state }, { visible }) {
      state.loader.visible = visible;
    },

  },


  actions: {

    LOADER_VISIBLE_CHANGE: true,


    LOADER_SHOW({ state, dispatch }) {
      if (!state.loader.visible) {
        dispatch('LOADER_VISIBLE_CHANGE', { visible: true });
      }
    },


    LOADER_HIDE({ state, dispatch }) {
      if (state.loader.visible) {
        dispatch('LOADER_VISIBLE_CHANGE', { visible: false });
      }
    },


    ACTION_CLICK_REQUEST_before: 'LOADER_SHOW',

    ATTACHMENT_EDIT_REQUEST_before: 'LOADER_SHOW',

    FOLDER_CREATE_REQUEST_before: 'LOADER_SHOW',

    FOLDER_RENAME_REQUEST_before: 'LOADER_SHOW',

    FILE_RENAME_REQUEST_before: 'LOADER_SHOW',

    IGNORED_FOLDER_CREATE_REQUEST_before: 'LOADER_SHOW',
    IGNORED_FOLDER_DELETE_REQUEST_before: 'LOADER_SHOW',

    FILE_EXTENSION_CREATE_REQUEST_before: 'LOADER_SHOW',
    FILE_EXTENSION_DELETE_REQUEST_before: 'LOADER_SHOW',

    COPY_REQUEST_before: 'LOADER_SHOW',

    MOVE_REQUEST_before: 'LOADER_SHOW',


    ACTION_CLICK_REQUEST_SUCCESS: 'LOADER_HIDE',
    ACTION_CLICK_REQUEST_ERROR: 'LOADER_HIDE',

    ATTACHMENT_EDIT_REQUEST_SUCCESS: 'LOADER_HIDE',
    ATTACHMENT_EDIT_REQUEST_ERROR: 'LOADER_HIDE',

    FOLDER_CREATE_REQUEST_SUCCESS: 'LOADER_HIDE',
    FOLDER_CREATE_REQUEST_ERROR: 'LOADER_HIDE',

    FOLDER_RENAME_REQUEST_SUCCESS: 'LOADER_HIDE',
    FOLDER_RENAME_REQUEST_ERROR: 'LOADER_HIDE',

    FILE_RENAME_REQUEST_SUCCESS: 'LOADER_HIDE',
    FILE_RENAME_REQUEST_ERROR: 'LOADER_HIDE',
    FILE_RENAME_REQUEST_PAID: 'LOADER_HIDE',

    IGNORED_FOLDER_CREATE_REQUEST_SUCCESS: 'LOADER_HIDE',
    IGNORED_FOLDER_CREATE_REQUEST_ERROR: 'LOADER_HIDE',
    IGNORED_FOLDER_CREATE_REQUEST_PAID: 'LOADER_HIDE',

    IGNORED_FOLDER_DELETE_REQUEST_SUCCESS: 'LOADER_HIDE',
    IGNORED_FOLDER_DELETE_REQUEST_ERROR: 'LOADER_HIDE',
    IGNORED_FOLDER_DELETE_REQUEST_PAID: 'LOADER_HIDE',

    FILE_EXTENSION_CREATE_REQUEST_SUCCESS: 'LOADER_HIDE',
    FILE_EXTENSION_CREATE_REQUEST_ERROR: 'LOADER_HIDE',
    FILE_EXTENSION_CREATE_REQUEST_PAID: 'LOADER_HIDE',

    FILE_EXTENSION_DELETE_REQUEST_SUCCESS: 'LOADER_HIDE',
    FILE_EXTENSION_DELETE_REQUEST_ERROR: 'LOADER_HIDE',
    FILE_EXTENSION_DELETE_REQUEST_PAID: 'LOADER_HIDE',

    COPY_REQUEST_SUCCESS: 'LOADER_HIDE',
    COPY_REQUEST_ERROR: 'LOADER_HIDE',

    MOVE_REQUEST_SUCCESS: 'LOADER_HIDE',
    MOVE_REQUEST_ERROR: 'LOADER_HIDE',

  },

};
